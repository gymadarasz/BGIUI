    // Cursor?
    Cursor cursor;
    cursor.reset(100);
    equ(cursor.getWidth(), 100, (char*)"Cursor reset: width ok?");
    equ(cursor.getTop(), 0, (char*)"Cursor reset: top ok?");
    equ(cursor.getLeft(), 0, (char*)"Cursor reset: left ok?");
    equ(cursor.getLineHeight(), 0, (char*)"Cursor reset: lineHeihgt ok?");
    cursor.step(60, 60);
    equ(cursor.getTop(), 0, (char*)"Cursor step: top ok?");
    equ(cursor.getLeft(), 60, (char*)"Cursor step: left ok?");
    equ(cursor.getLineHeight(), 60, (char*)"Cursor step: lineHeihgt ok?");
    cursor.step(60, 60);
    equ(cursor.getTop(), 60, (char*)"Cursor step2: top ok?");
    equ(cursor.getLeft(), 0, (char*)"Cursor step2: left ok?");
    equ(cursor.getLineHeight(), 60, (char*)"Cursor step2: lineHeihgt ok?");
    cursor.step(60, 60);
    equ(cursor.getTop(), 60, (char*)"Cursor step3: top ok?");
    equ(cursor.getLeft(), 60, (char*)"Cursor step3: left ok?");
    equ(cursor.getLineHeight(), 60, (char*)"Cursor step3: lineHeihgt ok?");
    cursor.br();
    equ(cursor.getTop(), 120, (char*)"Cursor break: top ok?");
    equ(cursor.getLeft(), 0, (char*)"Cursor break: left ok?");
